"""Чистая логика накопительной (лояльностной) скидки для варианта 4.

Магазин товаров для быта: величина скидки зависит от количества
уже совершённых покупок покупателя (любых товаров).

Логика вынесена в отдельный модуль без зависимости от Django,
чтобы её было просто покрывать модульными тестами.
"""

# Пары (минимальное число предыдущих покупок, процент скидки).
# Упорядочены от большего порога к меньшему.
DISCOUNT_TIERS = (
    (20, 15),
    (10, 10),
    (5, 5),
    (0, 0),
)

MAX_DISCOUNT_PERCENT = 15


def discount_for_purchase_count(count):
    """Вернуть процент скидки для покупателя с `count` покупками."""
    if count < 0:
        raise ValueError("Количество покупок не может быть отрицательным")
    for threshold, percent in DISCOUNT_TIERS:
        if count >= threshold:
            return percent
    return 0


def apply_discount(price, percent):
    """Вернуть цену после применения скидки `percent`, округлённую."""
    if price < 0:
        raise ValueError("Цена не может быть отрицательной")
    if not 0 <= percent <= 100:
        raise ValueError("Процент скидки должен быть в диапазоне 0..100")
    return round(price * (100 - percent) / 100)
